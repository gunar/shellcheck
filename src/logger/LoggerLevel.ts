/**
 * Logger level.
 */
export const enum LoggerLevel {
  /**
   * Off level.
   * Turn off logging.
   */
  OFF = 'off',
  /**
   * Debug level.
   */
  DEBUG = 'debug',
  /**
   * Informational level.
   */
  INFO = 'info',
  /**
   * Warning level.
   */
  WARN = 'warn',
  /**
   * Error level.
   */
  ERROR = 'error',
}
